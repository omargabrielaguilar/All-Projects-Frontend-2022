//Question 1

function btn1() {
    let qa_1 = document.querySelector(".qa-1")
    let qh3_1 = document.querySelector(".qh3-1")
    let icon_1 = document.querySelector(".icon-1")
    let ans_1 = document.querySelector(".ans-1")

    ans_1.style.visibility="visible"
    qa_1.style.paddingBottom = "5rem"
    qh3_1.style.color = "hsl(238, 29%, 16%)"
    icon_1.style.rotate = "180deg"
}

function close1() {
    let qa_1 = document.querySelector(".qa-1")
    let qh3_1 = document.querySelector(".qh3-1")
    let icon_1 = document.querySelector(".icon-1")
    let ans_1 = document.querySelector(".ans-1")

    ans_1.style.visibility="hidden"
    qa_1.style.paddingBottom = "1.8rem"
    qh3_1.style.color = "hsl(240, 6%, 50%)"
    icon_1.style.rotate = "0deg"
}


